package Model;

/**
 *
 * @author ACER
 */
public class Users {
    String id, name, account, pass;

    public Users() {
    }

    public Users(String id, String name, String account, String pass) {
        this.id = id;
        this.name = name;
        this.account = account;
        this.pass = pass;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
    
}
